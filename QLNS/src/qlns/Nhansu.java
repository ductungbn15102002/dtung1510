    private void btndanhdachnhansuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btndanhdachnhansuMouseClicked
       String sql = "SELECT `tendangnhap`, `matkhau`, `ho`, `ten`, `banla` FROM `nguoidung`";

    try {
        ps = conn.prepareStatement(sql);
        rs = ps.executeQuery();

        // Xóa toàn bộ dữ liệu trong bảng bangnhansu trước khi hiển thị mới
        DefaultTableModel model = (DefaultTableModel) bangnhansu.getModel();
        model.setRowCount(0);

        // Hiển thị từng dòng dữ liệu lấy từ cơ sở dữ liệu lên bảng
        while (rs.next()) {
            String tendangnhap = rs.getString("tendangnhap");
            String matkhau = rs.getString("matkhau");
            String ho = rs.getString("ho");
            String ten = rs.getString("ten");
            String banla = rs.getString("banla");

            // Thêm dòng dữ liệu vào bảng
            model.addRow(new Object[]{tendangnhap, matkhau, ho, ten, banla});
        }
    } catch (SQLException ex) {
        Logger.getLogger(dangky.class.getName()).log(Level.SEVERE, null, ex);
    }
        
    }//GEN-LAST:event_btndanhdachnhansuMouseClicked

