    private void btntimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntimkiemActionPerformed
       String keyword = txttimkiem.getText();
    if (keyword.trim().isEmpty()) {
        // Người dùng không nhập từ khóa tìm kiếm, không thực hiện tìm kiếm
        return;
    }

    // Thực hiện truy vấn đến cơ sở dữ liệu để tìm kiếm các bản ghi phù hợp
    try {
        String sql = "SELECT * FROM `nguoidung` WHERE `tendangnhap` LIKE ? OR `ho` LIKE ? OR `ten` LIKE ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, "%" + keyword + "%");
        ps.setString(2, "%" + keyword + "%");
        ps.setString(3, "%" + keyword + "%");
        ResultSet rs = ps.executeQuery();

        // Tạo DefaultTableModel để hiển thị dữ liệu trên bảng bangnhansu
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Tên đăng nhập");
        model.addColumn("Mật khẩu");
        model.addColumn("Họ");
        model.addColumn("Tên");
        model.addColumn("Chức vụ");

        while (rs.next()) {
            // Lấy thông tin từ ResultSet và thêm vào bảng
            String tendangnhap = rs.getString("tendangnhap");
            String matkhau = rs.getString("matkhau");
            String ho = rs.getString("ho");
            String ten = rs.getString("ten");
            String banla = rs.getString("banla");

            model.addRow(new Object[]{tendangnhap, matkhau, ho, ten, banla});
        }

        // Đóng kết nối và giải phóng tài nguyên
        rs.close();
        ps.close();

        // Set model cho bảng bangnhansu
        bangnhansu.setModel(model);
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Lỗi khi tìm kiếm dữ liệu trong cơ sở dữ liệu!");
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btntimkiemActionPerformed

    
