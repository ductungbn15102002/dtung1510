    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
     int selectedRow = bangnhansu.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Vui lòng chọn một dòng để sửa!");
        return;
    }

    // Lưu lại giá trị ban đầu của các ô cần sửa
    String tendangnhap = bangnhansu.getValueAt(selectedRow, 0).toString();
    String matkhau = bangnhansu.getValueAt(selectedRow, 1).toString();
    String ho = bangnhansu.getValueAt(selectedRow, 2).toString();
    String ten = bangnhansu.getValueAt(selectedRow, 3).toString();
    String banla = bangnhansu.getValueAt(selectedRow, 4).toString();

    // Hiển thị thông báo và nhập giá trị mới cho từng ô cần sửa
    String tendangnhapMoi = JOptionPane.showInputDialog(this, "Nhập tên đăng nhập mới:", tendangnhap);
    if (tendangnhapMoi == null) {
        // Người dùng bấm Cancel, không thực hiện sửa đổi
        return;
    }

    String matkhauMoi = JOptionPane.showInputDialog(this, "Nhập mật khẩu mới:", matkhau);
    if (matkhauMoi == null) {
        return;
    }

    String hoMoi = JOptionPane.showInputDialog(this, "Nhập họ mới:", ho);
    if (hoMoi == null) {
        return;
    }

    String tenMoi = JOptionPane.showInputDialog(this, "Nhập tên mới:", ten);
    if (tenMoi == null) {
        return;
    }

    // Show a dialog for selecting "banla" value
    String[] banlaOptions = {"Nhân viên", "Manager"}; // Replace with actual options
    String banlaMoi = (String) JOptionPane.showInputDialog(this, "Chọn banla mới:", "Chọn", JOptionPane.PLAIN_MESSAGE, null, banlaOptions, banla);
    if (banlaMoi == null) {
        return;
    }

    // Thực hiện cập nhật dữ liệu vào cơ sở dữ liệu XAMPP
    try {
        String sql = "UPDATE `nguoidung` SET `tendangnhap`=?, `matkhau`=?, `ho`=?, `ten`=?, `banla`=? WHERE `tendangnhap`=?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, tendangnhapMoi);
        ps.setString(2, matkhauMoi);
        ps.setString(3, hoMoi);
        ps.setString(4, tenMoi);
        ps.setString(5, banlaMoi);
        ps.setString(6, tendangnhap);

        int rowsAffected = ps.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Đã cập nhật thông tin thành công!");
            // Sau khi cập nhật thành công, cần cập nhật lại dữ liệu trên bảng
            btndanhdachnhansuMouseClicked(null);
        } else {
            JOptionPane.showMessageDialog(null, "Không thành công. Lỗi khi cập nhật dữ liệu!");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Lỗi khi cập nhật dữ liệu vào cơ sở dữ liệu!");
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btnsuaActionPerformed

